										<p>Team 1676 proudly spreads the message of <i>FIRST</i> throughout their school district. The recruiting process begins early&mdash;just as sports leagues employ teams for young children to work their way up to high school teams, the Pi-oneers do the same with robotics. Young students in elementary school are encouraged to join the FLL Jr. teams at the library; they meet the team during their Brownie or Cub Scout meetings or attend the Pi-oneer science programs held at their local library. Many students have joined the FLL teams as a result of attending LEGOs &amp; a Movie, held for those in Kindergarten through Grade six. This year more students and their parents will meet the team at the River Vale Innovation &amp; Science Fair. Many parents look forward to team events and plan to have their child join <i>FIRST</i> Robotics in middle school.</p>
										<p>The Pi-oneers mentor the six FLL teams in their district which in turn is an excellent source of new membership. During the Bergen FLL Qualifying Tournament run by the team, future members witness the teamwork, dedication and fun had by the Pi-oneers. The team also includes the 6 teams in the Annual Fall Carwash each year to bond with them and provide encouragement to join the team when they arrive in high school. Team 1676 provides a program during Eighth Grade Orientations at both high schools in order to introduce incoming freshmen to their team. In the spring, they visit the middle schools to give a presentation explaining what <i>FIRST</i> is, who the Pi-oneers are and what the team does. The team visits with Girl Scout troops and Boy Scout troops, and even though they may not all be in the teamâ€™s school district, the Pi-oneers promote <i>FIRST</i> membership.</p>
										<p>Team 1676 participates in not only Back-to-School Nights at each high school introducing parents to the team and <i>FIRST</i>, but also Freshman Orientation Days. Contacted by the team liaison to the FLL teams, the FLL coaches send a list of their graduates to ensure the Pi-oneers reach out to them personally. At the beginning of each new school year, team members visit freshman Biology classes to recruit new members. New this year is a recruitment video shown during the presentation. Almost thirty new members initially joined the team, consisting of both freshmen and upperclassmen, proving it is never too late to become a Pi-oneer! Team members recruit their friends, and siblings to become members after learning about the team at home and from visiting on weekends.</p>
										<p>The Pi-oneers meet potential <i>FIRST</i> members at every event they attend. For many years the team has participated in two local Memorial Day Parades which results in team recognition for other events. During the local town days the team not only provides robot demonstrations but also hands out literature for all levels of <i>FIRST</i>. Many students have been recruited to join the local teams, and parents eagerly listen to the details.</p>
										<p>The Pascack Pi-oneers realize their success lies in not only the present but also the future. The younger generation is the key to team sustainability and the team has made them their focus.</p>
