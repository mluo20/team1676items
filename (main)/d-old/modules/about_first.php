                    <div class="text" style="position: relative;">
                        As a <i>FIRST</i> Robotics Competition (FRC) Team, the Pascack Pi-oneers annually design and construct a robot to participate in the current year’s game within the six weeks of Build Season. <i>FIRST</i> is more than robots, providing opportunities for team members to contribute to their team by non-mechanical means.<br>
                        <br>
                        The goal of Team 1676 is to meet the challenges of FRC by valuing teamwork, camaraderie, community service, Gracious Professionalism and Coopertition. Through team participation, members attain confidence from hands-on experiences lead by dedicated mentors. They learn leadership skills and are inspired to mentor others during Build Season and throughout the year.<br>
                        <br>
                        Team members constantly strive to educate others in STEAM subjects, with a focus on children, the future of STEAM and <i>FIRST</i>. The Pi-oneers are passionate about what they do and apply the concepts practiced at robotics to everyday life. The team’s motto is, “Gracious Professionalism ... Don’t Leave High School Without It!
